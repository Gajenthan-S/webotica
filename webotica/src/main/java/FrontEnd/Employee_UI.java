    private void Employee_TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Employee_TableMouseClicked
        try {
            int row = Employee_Table.getSelectedRow();
            String Table_CLick = (Employee_Table.getModel().getValueAt(row, 0).toString());
            String sql = "select * from Employees where Employee_ID='" + Table_CLick + "'";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {
                String add1 = rs.getString("Employee_ID");
                txt_IDNumber.setText(add1);

                String add2 = rs.getString("Name");
                txt_Name.setText(add2);
                String add3 = rs.getString("Address");
                txt_Address.setText(add3);
                String add4 = rs.getString("Mail_Address");
                txt_MailAddress.setText(add4);
                String add5 = rs.getString("Job_Category");
                combo_JobCategory.setToolTipText(add5);
                String add6 = rs.getString("Salary");
                txt_Salary.setText(add6);
                String add7 = rs.getString("Contact_Number");
                txt_ContactNumber.setText(add7);
                String add8 = rs.getString("Age");
                txt_Age.setText(add8);
                String add9 = rs.getString("NIC");
                txt_NIC.setText(add9);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }        // TODO add your handling code here:

        // TODO add your handling code here:
    }//GEN-LAST:event_Employee_TableMouseClicked

