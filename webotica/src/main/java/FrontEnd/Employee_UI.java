    private void cmd_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_AddActionPerformed
        try {

            String sql = "insert into Employees (Employee_ID,Name,Address,Mail_Address,Job_Category,Salary,Contact_Number,NIC,Age) values(?,?,?,?,?,?,?,?,?)";
            pst = conn.prepareStatement(sql);
            pst.setString(1, txt_IDNumber.getText());
            pst.setString(2, txt_Name.getText());
            pst.setString(3, txt_Address.getText());
            pst.setString(4, txt_MailAddress.getText());
            String value1 = combo_JobCategory.getSelectedItem().toString();
            pst.setString(5, value1);
            pst.setString(6, txt_Salary.getText());
            pst.setString(7, txt_Age.getText());
            pst.setString(8, txt_ContactNumber.getText());
            pst.setString(9, txt_NIC.getText());

            pst.execute();
            JOptionPane.showMessageDialog(null, "Added");
        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, e);
        }
        Update_Table();        // TODO add your handling code here:
    }//GEN-LAST:event_cmd_AddActionPerformed

